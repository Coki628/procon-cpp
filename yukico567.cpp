/**
 * ・自力AC！
 * ・確率DP
 * ・6つの面が出たかの2^6通りを状態に持って6通り遷移する。
 * ・100万*64*6=3億8400万は絶対想定解じゃなさそう、と思いつつもC++で。
 * ・実際最大ケースは手元でも全然間に合わない。。
 * ・けどよく考えたら、それだけやったらほぼ100%だな、と思って試しに1万回で試したら
 * 　やっぱり1って出たので、N=10000以上なら全部1にする例外処理で無事AC。
 */

